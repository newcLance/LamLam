# 黑话黑名单（worldview 不能出现）
BLACK_LIST_WV = ["本作", "该作", "该游戏", "玩家将", "以...为核心", "通过...的方式",
                 "呈现出", "营造出", "塑造出", "诠释着", "诠释了",
                 "综上所述", "值得一提的是", "不仅", "构筑", "构建", "演绎", "承载"]

# overview 禁用词
BLACK_LIST_OV = ["本作", "该作", "该游戏", "玩家将", "在...的世界中",
                 "在...的设定下", "以...为背景", "讲述了", "围绕...展开",
                 "精彩绝伦", "史诗般的", "传奇巨作", "经典之作"]

# summary.evolution 禁用词
BLACK_LIST_EV = ["由来已久", "自古以来", "源远流长", "致敬经典"]


def check(slug, payload):
    issues = []
    wv = payload["worldview"]
    for k, card in wv.items():
        val = card["value"]
        plain = re.sub(r"<[^>]+>", "", val)
        bolds = re.findall(r"<b>(.*?)</b>", val)
        if not (120 <= len(plain) <= 220):
            issues.append(f"  ⚠️ worldview.{k} 字数={len(plain)}（需 120-220）")
        if len(bolds) < 2:
            issues.append(f"  ❌ worldview.{k} <b>={len(bolds)}（需≥2）")
        if "你" not in val:
            issues.append(f"  FAIL worldview.{k} no second-person")
        for w in BLACK_LIST_WV:
            if w in val:
                issues.append(f"  ❌ worldview.{k} 命中黑话词: {w}")

    ov = payload["story_overview"]
    tag_plain = re.sub(r"<[^>]+>", "", ov["tagline"])
    if not (12 <= len(tag_plain) <= 30):
        issues.append(f"  ⚠️ tagline 字数={len(tag_plain)}（需 12-30）")
    total_chars = 0
    total_bolds = len(re.findall(r"<b>", ov["tagline"]))
    for i, p in enumerate(ov["paragraphs"], 1):
        plain = re.sub(r"<[^>]+>", "", p)
        bolds = len(re.findall(r"<b>", p))
        total_chars += len(plain)
        total_bolds += bolds
        if not (120 <= len(plain) <= 220):
            issues.append(f"  ⚠️ overview 段{i} 字数={len(plain)}")
    if not (380 <= total_chars <= 600):
        issues.append(f"  ⚠️ overview 总字数={total_chars}（需 380-600）")
    if total_bolds < 6:
        issues.append(f"  ❌ overview 全篇 <b>={total_bolds}（需≥6）")
    last = ov["paragraphs"][-1]
    if not last.rstrip().endswith(("？", "?", "…", "...")):
        issues.append(f"  ❌ overview 第3段末尾未以问号/省略号收尾")
    if "你" not in ov["paragraphs"][1]:
        issues.append("  FAIL overview p2 no second-person")
    for w in BLACK_LIST_OV:
        for p in ov["paragraphs"]:
            if w in p:
                issues.append(f"  ❌ overview 命中禁用词: {w}")
                break

    su = payload["aesthetic_summary"]
    if not (6 <= len(su["label"]) <= 30):
        issues.append(f"  ⚠️ summary.label 长度={len(su['label'])}")
    df_plain = re.sub(r"<[^>]+>", "", su["definition"])
    if not (120 <= len(df_plain) <= 200):
        issues.append(f"  ⚠️ definition 字数={len(df_plain)}（需 120-200）")
    if "\u00d7" not in su["definition"]:
        issues.append("  WARN definition no high-concept x")
    ev_plain = re.sub(r"<[^>]+>", "", su["evolution"])
    if not (150 <= len(ev_plain) <= 380):
        issues.append(f"  ⚠️ evolution 字数={len(ev_plain)}（需 150-380）")
    for w in BLACK_LIST_EV:
        if w in su["evolution"]:
            issues.append(f"  ❌ evolution 命中禁用词: {w}")
    refs = su["references"]
    if not (5 <= len(refs) <= 8):
        issues.append(f"  ❌ references 数量={len(refs)}（需 5-8）")
    types = set(r["type"] for r in refs)
    if len(types) < 3:
        issues.append(f"  ❌ references type 多样性={len(types)}（需≥3）")

    return issues


def main():
    print("=" * 70)
    print("v2.5 第一批 5 款游戏内容写入 + 自检")
    print("=" * 70)
    for slug, payload in GAMES.items():
        fp = DATA_DIR / f"{slug}.json"
        with open(fp, "r", encoding="utf-8") as f:
            d = json.load(f)

        # 1) worldview 整段插入（位置：story 之后、aesthetic 之前）
        # 2) story.overview 插入（删除遗留 title）
        # 3) aesthetic.summary 插入到顶部
        # 4) aesthetic 5 维加 anchorRule

        # --- worldview ---
        d["worldview"] = payload["worldview"]

        # --- story.overview ---
        story = d.get("story", {})
        if "title" in story:
            del story["title"]
        new_story = {"overview": payload["story_overview"]}
        if "dims" in story:
            new_story["dims"] = story["dims"]
        for k, v in story.items():
            if k not in new_story:
                new_story[k] = v
        d["story"] = new_story

        # --- aesthetic ---
        aes = d.get("aesthetic", {})
        new_aes = {"summary": payload["aesthetic_summary"]}
        for k in ["scene", "costume", "ui", "symbol", "promo"]:
            if k in aes and isinstance(aes[k], dict):
                card = dict(aes[k])
                card["anchorRule"] = payload["aesthetic_anchor"][k]
                new_aes[k] = card
        if "rivals" in aes:
            new_aes["rivals"] = aes["rivals"]
        for k, v in aes.items():
            if k not in new_aes:
                new_aes[k] = v
        d["aesthetic"] = new_aes

        # 顺序调整：把 worldview 放在 story 之前
        new_order = {}
        for k in ["id", "slug"]:
            if k in d: new_order[k] = d[k]
        if "playerExp" in d: new_order["playerExp"] = d["playerExp"]
        if "worldview" in d: new_order["worldview"] = d["worldview"]
        if "story" in d: new_order["story"] = d["story"]
        if "aesthetic" in d: new_order["aesthetic"] = d["aesthetic"]
        for k, v in d.items():
            if k not in new_order:
                new_order[k] = v

        with open(fp, "w", encoding="utf-8") as f:
            json.dump(new_order, f, ensure_ascii=False, indent=2)

        print(f"\n[{slug}]")
        issues = check(slug, payload)
        if issues:
            for i in issues:
                print(i)
        else:
            print("  ✅ 全部硬规则通过")


if __name__ == "__main__":
    main()

def write_one(slug, payload):
    fp = DATA_DIR / f"{slug}.json"
    with open(fp, "r", encoding="utf-8") as f:
        d = json.load(f)
    d["worldview"] = payload["worldview"]
    story = d.get("story", {})
    if "title" in story:
        del story["title"]
    new_story = {"overview": payload["story_overview"]}
    if "dims" in story:
        new_story["dims"] = story["dims"]
    for k, v in story.items():
        if k not in new_story:
            new_story[k] = v
    d["story"] = new_story
    aes = d.get("aesthetic", {})
    new_aes = {"summary": payload["aesthetic_summary"]}
    for k in ["scene", "costume", "ui", "symbol", "promo"]:
        if k in aes and isinstance(aes[k], dict):
            card = dict(aes[k])
            card["anchorRule"] = payload["aesthetic_anchor"][k]
            new_aes[k] = card
    if "rivals" in aes:
        new_aes["rivals"] = aes["rivals"]
    for k, v in aes.items():
        if k not in new_aes:
            new_aes[k] = v
    d["aesthetic"] = new_aes
    new_order = {}
    for k in ["id", "slug"]:
        if k in d: new_order[k] = d[k]
    if "playerExp" in d: new_order["playerExp"] = d["playerExp"]
    if "worldview" in d: new_order["worldview"] = d["worldview"]
    if "story" in d: new_order["story"] = d["story"]
    if "aesthetic" in d: new_order["aesthetic"] = d["aesthetic"]
    for k, v in d.items():
        if k not in new_order:
            new_order[k] = v
    with open(fp, "w", encoding="utf-8") as f:
        json.dump(new_order, f, ensure_ascii=False, indent=2)


def check(slug):
    with open(DATA_DIR / f"{slug}.json", "r", encoding="utf-8") as f: d = json.load(f)
    issues = []
    wv = d.get("worldview", {})
    for k in ["background","mainStory","combat","coreLoop"]:
        c = wv.get(k, {}); val = c.get("value","")
        plain = re.sub(r"<[^>]+>", "", val)
        bolds = re.findall(r"<b>", val)
        if not (120 <= len(plain) <= 240): issues.append(f"wv.{k}={len(plain)}")
        if len(bolds) < 2: issues.append(f"wv.{k} bolds={len(bolds)}")
        if "\u4f60" not in val: issues.append(f"wv.{k} no second-person")
    ov = d.get("story",{}).get("overview", {})
    paras = ov.get("paragraphs",[])
    total = sum(len(re.sub(r"<[^>]+>","",p)) for p in paras)
    bolds = sum(len(re.findall(r"<b>",p)) for p in paras)+len(re.findall(r"<b>",ov.get("tagline","")))
    if not (380<=total<=750): issues.append(f"ov_total={total}")
    if bolds<6: issues.append(f"ov_bolds={bolds}")
    if paras and not paras[-1].rstrip().endswith(("\uff1f","?","\u2026","...")): issues.append("p3 no ?")
    su = d.get("aesthetic",{}).get("summary",{})
    if su:
        df = re.sub(r"<[^>]+>","",su.get("definition",""))
        if not (120<=len(df)<=230): issues.append(f"def={len(df)}")
        ev = re.sub(r"<[^>]+>","",su.get("evolution",""))
        if not (150<=len(ev)<=380): issues.append(f"ev={len(ev)}")
        refs = su.get("references", [])
        types = set(r["type"] for r in refs)
        if len(types)<3: issues.append(f"types={len(types)}")
        years_str=" ".join(r.get("year","") for r in refs)
        years = [int(y) for y in re.findall(r"(1[89]\d{2}|20\d{2})", years_str)]
        if years and max(years)-min(years)<20: issues.append(f"span={max(years)-min(years)}")
    return issues


def main():
    print("=== v2.5 Batch 03: 6 shooter games ===")
    for slug, payload in GAMES.items():
        write_one(slug, payload)
        issues = check(slug)
        if issues:
            print(f"\n[{slug}] {len(issues)} issues: {issues}")
        else:
            print(f"\n[{slug}] PASS")


if __name__ == "__main__":
    main()
